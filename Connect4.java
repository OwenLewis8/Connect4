    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        JButton thisOne = (JButton) evt.getSource();

        if (startGame == true) {
            if (thisOne.getText().equals("") && (p1 == true)) {
                thisOne.setText("X");
                startButton.setText("Player 2 Turn");
                switch (p1Color) {
                    case 1:
                        thisOne.setBackground(Color.red);
                        thisOne.setForeground(Color.red);
                        break;
                    case 2:
                        thisOne.setBackground(Color.yellow);
                        thisOne.setForeground(Color.yellow);
                        break;
                    case 3:
                        thisOne.setBackground(Color.green);
                        thisOne.setForeground(Color.green);
                        break;
                }
                p1 = false;
                p2 = true;
            } else if (thisOne.getText().equals("") && (p2 == true)) {
                thisOne.setText("O");
                startButton.setText("Player 3 Turn");
                switch (p2Color) {
                    case 1:
                        thisOne.setBackground(Color.red);
                        thisOne.setForeground(Color.red);
                        break;
                    case 2:
                        thisOne.setBackground(Color.yellow);
                        thisOne.setForeground(Color.yellow);
                        break;
                    case 3:
                        thisOne.setBackground(Color.green);
                        thisOne.setForeground(Color.green);
                        break;
                }
                p2 = false;
                p3 = true;
            } else if (thisOne.getText().equals("") && (p3 == true)) {
                thisOne.setText("Y");
                startButton.setText("Player 1 Turn");

                switch (p3Color) {
                    case 1:
                        thisOne.setBackground(Color.red);
                        thisOne.setForeground(Color.red);
                        break;
                    case 2:
                        thisOne.setBackground(Color.yellow);
                        thisOne.setForeground(Color.yellow);
                        break;
                    case 3:
                        thisOne.setBackground(Color.green);
                        thisOne.setForeground(Color.green);
                        break;
                }
                p1 = true;
                p3 = false;
            }

            move++;
            try {
                checkForWin();
            } catch (NoSuchFieldException ex) {
                Logger.getLogger(Connect4.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IllegalArgumentException ex) {
                Logger.getLogger(Connect4.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IllegalAccessException ex) {
                Logger.getLogger(Connect4.class.getName()).log(Level.SEVERE, null, ex);
            }
        }


    }//GEN-LAST:event_jButton1ActionPerformed

