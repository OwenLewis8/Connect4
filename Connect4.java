    /*
    clearBoard()    clears grid
    param buttonArray   2D array of grid
    */
    private void clearBoard() throws NoSuchFieldException, IllegalArgumentException, IllegalAccessException {

        for (int row = 0; row < 8; row++) {
            for (int col = 0; col < 8; col++) {
                buttonArray[row][col].setText("");
                buttonArray[row][col].setBackground(Color.white);
            }
        }
    }

    /*
    cancelGame()    sets everything to pre startbutton conditions
    */
    private void cancelGame() throws NoSuchFieldException, IllegalArgumentException, IllegalAccessException {

        clearBoard();
        disableButtons();
        enableColorButton();
        startButton.setText("Start Game");
        p1 = true;
        p2 = false;
        p3 = false;
        board = "";
        move = 0;

    }

    //disables grid
    private void disableButtons() {

        for (int row = 0; row < 8; row++) {
            for (int col = 0; col < 8; col++) {
                buttonArray[row][col].setEnabled(false);
            }

        }
    }

    //enables grid
    private void enableButtons() {

        for (int row = 0; row < 8; row++) {
            for (int col = 0; col < 8; col++) {
                buttonArray[row][col].setEnabled(true);
            }

        }
    }
    
